public class SmillingCat {
    public void displaySmilingcat () {
        System.out.println("   /\\       /\\  ");
        System.out.println("  /  \\ ____/  \\ ");
        System.out.println(" /             \\ ");
        System.out.println("(   /\\      /\\  )");

        /* 
         Dalam Java, karakter backslash adalah karakter escape yang digunakan untuk 
        menulis karakter khusus seperti newline 
        (\n), tab (\t), dll. 
        Jadi, jika Anda ingin mencetak karakter backslash itu sendiri, 
        Anda perlu menggunakan dua backslash berturut-turut (\).
        */
        
        System.out.println(" =====   V   ====");
        System.out.println("=====(___|___)=====");
        System.out.println(" (             )");
        System.out.println("  (___________)");
   
        System.out.println("KUCING OLEENN !");
    }
}
